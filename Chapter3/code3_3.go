package main
import "fmt"

func main() {
    var x string // Declaration of a New Variable of Type String
    x = "First" // Assigning Value to the New Variable
    fmt.Println(x)
    x = "Second" // Assigning a New Value to the Variable x
    fmt.Println(x)
}
